package CWT;

public class MethodForCalculation {
	
	public void add(int a , int b) {
		System.out.println(a+b);
		
	}
	public void sub(int a,int b) {
		System.out.println(a-b);
		
	}
	public void multiply(int a ,int b) {
		System.out.println(a*b);
		
	} 
	public static void methodforcalculation(String[] args) {
	 MethodForCalculation obj = new MethodForCalculation();
	 obj.add(12, 5);
	 obj.sub(2, 3);
	 obj.multiply(4, 5);
	}
}
		
	
	
	
	
		
		
	


